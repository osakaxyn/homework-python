"""
Робот может перемещаться в четырех направлениях
(«С» — север, «З» — запад, «Ю» — юг, «В» — восток)
и принимать три цифровые команды:
0 — продолжать движение, 1 — поворот налево, −1 — поворот направо.
Дан символ C — исходное направление робота
и целое число N — посланная ему команда.
Вывести направление робота после выполнения полученной команды.
"""


def new_direction(C, N):
    directions = ['С', 'В', 'Ю', 'З']  # Север, Восток, Юг, Запад
    index = directions.index(C)  # Определяем текущий индекс направления

    match N:
        case 1:
            index = (index - 1) % 4  # Поворот налево
        case -1:
            index = (index + 1) % 4  # Поворот направо
        case 0:
            pass  # Продолжаем движение

    return directions[index]

# Ввод данных с защитой от ошибок
C = input("Введите исходное направление (С, З, Ю, В): ").strip().upper()
while C not in ['С', 'З', 'Ю', 'В']:
    print("Ошибка! Введите корректное направление: С, З, Ю или В.")
    C = input("Введите исходное направление (С, З, Ю, В): ").strip().upper()

while True:
    try:
        N = int(input("Введите команду (0, 1, -1): ").strip())
        if N in [0, 1, -1]:
            break
        else:
            print("Ошибка! Введите только 0, 1 или -1.")
    except ValueError:
        print("Ошибка! Введите целое число (0, 1 или -1).")

# Вывод результата
print("Новое направление:", new_direction(C, N))
